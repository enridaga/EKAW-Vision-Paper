import csv
from collections import defaultdict

# Same source CSVs and conventions as bin/make-tables.py:
# each CSV is a list of "KG, feature" rows, one file per dimension.
DIMENSIONS = [
    ('Semantic property',   'data/semantic_properties.csv'),
    ('Semantic affordance', 'data/semantic_affordances.csv'),
    ('Pragmatic property',  'data/pragmatic_properties.csv'),
    ('Pragmatic affordance','data/pragmatic_affordances.csv'),
]

def load_csv(filepath):
    """KG -> list of features, preserving file order; also returns KG first-appearance order."""
    data = defaultdict(list)
    order = []
    with open(filepath, newline='', encoding='utf-8') as f:
        for row in csv.reader(f):
            if len(row) == 2:
                kg, feat = row[0].strip(), row[1].strip()
                if kg not in order:
                    order.append(kg)
                if feat not in data[kg]:
                    data[kg].append(feat)
    return data, order

def main():
    dim_data = []
    kg_order = []
    for _, path in DIMENSIONS:
        data, order = load_csv(path)
        dim_data.append(data)
        for kg in order:
            if kg not in kg_order:
                kg_order.append(kg)   # row order = first appearance across files

    col_spec = 'p{0.10\\textwidth}' + ' p{0.22\\textwidth}'+ ' p{0.22\\textwidth}'+ ' p{0.12\\textwidth}'+ ' p{0.22\\textwidth}' #* len(DIMENSIONS)

    lines = []
    lines.append(r'% Combined feature table, auto-generated by bin/make-combined-table.py')
    lines.append(r'% Do not edit by hand: regenerate from data/*.csv')
    lines.append(r'\begin{table}[t]')
    lines.append(r'\centering')
    lines.append(r'\scriptsize')
    lines.append(r'\setlength{\tabcolsep}{4pt}')
    lines.append(r'\renewcommand{\arraystretch}{1.2}')
    lines.append(r'\caption{Provenance features of the ten knowledge graphs across the '
                 r'four characterisation dimensions. Each cell lists the features '
                 r'exhibited by that KG in that dimension. Generated from the same data '
                 r'as the per-dimension matrices.}')
    lines.append(r'\label{tab:provenance-combined}')
    lines.append(r'\begin{tabular}{' + col_spec + r'}')
    lines.append(r'\toprule')
    header = r'\textbf{KG} & ' + ' & '.join(
        r'\textbf{' + name + '}' for name, _ in DIMENSIONS)
    lines.append(header + r' \\')
    lines.append(r'\midrule')

    for kg in kg_order:
        cells = [kg]
        for data in dim_data:
            feats = data.get(kg, [])
            cells.append(', '.join(feats) if feats else '---')
        lines.append(' & '.join(cells) + r' \\')
        lines.append(r'\addlinespace')

    lines.append(r'\bottomrule')
    lines.append(r'\end{tabular}')
    lines.append(r'\end{table}')

    out = '\n'.join(lines) + '\n'
    with open('Tables/combined_table.tex', 'w', encoding='utf-8') as f:
        f.write(out)
    print(out)
    print('--- written to Tables/combined_table.tex ---')

if __name__ == '__main__':
    main()